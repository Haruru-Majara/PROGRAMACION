package Ejercicio9;

public class Carta {
	private String apellido;
	private String nombre;
	private String ciudad;
	private String pais;
	
	
	public Carta(String apellido, String nombre, String ciudad, String pais) {
		this.apellido = apellido;
		this.nombre = nombre;
		this.ciudad = ciudad;
		this.pais = pais;
	}
	
	
	
}
